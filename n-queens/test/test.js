var chai = require('chai');
var run = require('../src/n-queens.js');

describe('Nqueens', function() {
    it('simple 8x8 test must equal to false', function() {
        var s=[ 
        [ '_', '_', '*', '_', '_', '_', '_', '_' ],
        [ '_', '_', '_', '_', '_', '_', '_', '*' ],
        [ '_', '*', '_', '_', '_', '_', '_', '_' ],
        [ '_', '_', '_', '_', '_', '*', '_', '_' ],
        [ '_', '_', '_', '_', '*', '_', '_', '_' ],
        [ '_', '_', '_', '_', '*', '_', '_', '_' ],
        [ '_', '_', '*', '_', '_', '_', '_', '_' ],
        [ '*', '_', '_', '_', '_', '_', '_', '_' ] ];
        var res = [ 
        [ '_', '_', '_', '_', '_', '_', '*', '_' ],
        [ '_', '_', '_', '*', '_', '_', '_', '_' ],
        [ '*', '_', '_', '_', '_', '_', '_', '_' ],
        [ '_', '_', '_', '_', '_', '_', '_', '*' ],
        [ '_', '*', '_', '_', '_', '_', '_', '_' ],
        [ '_', '_', '_', '_', '*', '_', '_', '_' ],
        [ '_', '_', '*', '_', '_', '_', '_', '_' ],
        [ '*', '_', '_', '_', '_', '_', '_', '_' ] ];
        var result = run(s,8,300);
        chai.assert.equal(false,result[0]);
        chai.assert.deepEqual(res,result[1]);
    })
    it('simple 4x4 test must equal to true', function() {
    var s= [ 
    [ '_', '_', '_', '*' ],
    [ '_', '_', '_', '*' ],
    [ '_', '_', '*', '_' ],
    [ '_', '*', '_', '_' ] ]
    var res =[ 
    [ '_', '_', '*', '_' ],
    [ '*', '_', '_', '_' ],
    [ '_', '_', '_', '*' ],
    [ '_', '*', '_', '_' ] ]
    var result = run(s,4,300);
    chai.assert.equal(true,result[0]);
    chai.assert.deepEqual(res,result[1]);
    })
});